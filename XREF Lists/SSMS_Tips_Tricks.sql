Enable/Disable Line Numbers 
By default, SSMS does not show line numbers. To enable them, follow these steps: 
Open the Tools menu and select Options.
In the left panel of the Options dialog box, expand the Text Editor option.
Expand All Languages (or specifically Transact-SQL for SQL files only).
Select General.
In the right panel, check the box for Line numbers.
Click OK to save the changes. The line numbers will appear in new query windows you open. 
